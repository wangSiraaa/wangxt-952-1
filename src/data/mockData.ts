const now = new Date();

export const initialSeats: Seat[] = Array.from({ length: 20 }, (_, i) => {
  const row = Math.floor(i / 5) + 1;
  const col = (i % 5) + 1;
  let status: Seat['status'] = 'available';
  let patientId: string | null = null;
  let disinfectionStartedAt: Date | null = null;

  if (i < 8) {
    status = 'occupied';
    patientId = `P${String(i + 1).padStart(3, '0')}`;
  } else if (i >= 8 && i < 10) {
    status = 'disinfecting';
    disinfectionStartedAt = new Date(now.getTime() - (i - 8) * 5 * 60 * 1000);
  } else if (i === 19) {
    status = 'maintenance';
  }

  return {
    id: `S${String(i + 1).padStart(3, '0')}`,
    row,
    col,
    status,
    patientId,
    disinfectionStartedAt,
    disinfectionDuration: 15,
  };
});

